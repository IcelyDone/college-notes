import turtle
hitomi = turtle.Turtle()

for i in range(36):
    hitomi.forward(100)
    hitomi.left(145)
    hitomi.forward(10)
    hitomi.right(90)
    hitomi.forward(10)
    hitomi.left(135)
    hitomi.forward(100)

    

